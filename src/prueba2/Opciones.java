    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
         
       
       
        //simulador
         Dibujo_Diagrama datos = new Dibujo_Diagrama();
         datos.setVelocidad(Double.parseDouble(Velocidad_Incial.getText()));
         datos.setAngulo(Double.parseDouble(Angulo.getText()));         
         datos.setAltura(Double.parseDouble(Altura.getText()));        
         datos.setGravedad(Double.parseDouble(Gravedad.getText()));
         datos.setMasa(Double.parseDouble(Masa.getText()));
         datos.setViento(Double.parseDouble(Viento.getText()));
         //operaciones 
         Operaciones datos2 = new Operaciones();
         datos2.setMasa(Double.parseDouble(Masa.getText()));
         datos2.setViento(Double.parseDouble(Viento.getText()));
         datos2.setTiempo(Double.parseDouble(Tiempo.getText()));
         datos2.setVelocidad(Double.parseDouble(Velocidad_Incial.getText()));
         datos2.setAngulo(Double.parseDouble(Angulo.getText()));         
         datos2.setAltura(Double.parseDouble(Altura.getText()));        
         datos2.setGravedad(Double.parseDouble(Gravedad.getText()));
         
         //Mostrar
         MenuResultados datos3 = new MenuResultados();
         datos3.setVelocidadInsta(datos2.VelocidadInstaResTiempo());
         datos3.setAnguloinsta(String.valueOf(datos2.AnguloInsta()));
         datos3.setDistanciaRecorrida(String.valueOf(datos2.DistanciaT()));
         datos3.setAlturainsta((String.valueOf(datos2.AlturaInstan())));
         datos3.setDistanciaMax((String.valueOf(datos2.DistanciaMax())));
         datos3.setAluraMax((String.valueOf(datos2.AlturaMax())));
         datos3.setEnergiaCine((String.valueOf(datos2.EnergiaCineInsta())));
         datos3.setEnergiaPO((String.valueOf(datos2.EnergiaPotencial())));
         datos3.setTrabajoViento((String.valueOf(datos2.trabajoViento())));
         datos3.setEnergiaMeca((String.valueOf(datos2.EnergiaMecanica())));
         JOptionPane.showMessageDialog(null, "Datos Procesados ");
         
    }                                        

